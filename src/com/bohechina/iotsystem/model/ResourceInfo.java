package com.bohechina.iotsystem.model;

public class ResourceInfo  {

    private int id;
    private int no;
    private String subject;
	private String resName;
	private String resPinyin;
	private String resFun;
	private String resApp;
	private String resVrp;
	private String resNum;
	private String resImg;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getResName() {
		return resName;
	}
	public void setResName(String resName) {
		this.resName = resName;
	}
	public String getResPinyin() {
		return resPinyin;
	}
	public void setResPinyin(String resPinyin) {
		this.resPinyin = resPinyin;
	}
	public String getResFun() {
		return resFun;
	}
	public void setResFun(String resFun) {
		this.resFun = resFun;
	}
	public String getResApp() {
		return resApp;
	}
	public void setResApp(String resApp) {
		this.resApp = resApp;
	}
	public String getResVrp() {
		return resVrp;
	}
	public void setResVrp(String resVrp) {
		this.resVrp = resVrp;
	}
	public String getResNum() {
		return resNum;
	}
	public void setResNum(String num) {
		this.resNum = num;
	}
	public String getResImg() {
		return resImg;
	}
	public void setResImg(String resImg) {
		this.resImg = resImg;
	}

}
