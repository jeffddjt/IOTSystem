package com.bohechina.iotsystem.model;

public class Trouble{
	
	private int id;
	private int no;
	private String name;
	private String study_console;
	private String study_scene;
	private String test_console;
	private String test_scene;
	private String exam_console;
	private String exam_scene;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStudy_console() {
		return study_console;
	}
	public void setStudy_console(String study_console) {
		this.study_console = study_console;
	}
	public String getStudy_scene() {
		return study_scene;
	}
	public void setStudy_scene(String study_scene) {
		this.study_scene = study_scene;
	}
	public String getTest_console() {
		return test_console;
	}
	public void setTest_console(String test_console) {
		this.test_console = test_console;
	}
	public String getTest_scene() {
		return test_scene;
	}
	public void setTest_scene(String test_scene) {
		this.test_scene = test_scene;
	}
	public String getExam_console() {
		return exam_console;
	}
	public void setExam_console(String exam_console) {
		this.exam_console = exam_console;
	}
	public String getExam_scene() {
		return exam_scene;
	}
	public void setExam_scene(String exam_scene) {
		this.exam_scene = exam_scene;
	}
	
	
	
}
